#!/bin/sh
# alembic не добавляет текущую папку в sys.path (uvicorn добавляет), поэтому на
# Timeweb, где рабочая директория /app/smartweb/backend, нужен явный PYTHONPATH.
export PYTHONPATH="$(pwd):${PYTHONPATH}"

# Миграции — СТРОГО В ФОНЕ: сервер стартует немедленно и всегда отвечает на
# health-check, даже если миграции идут долго (напр. создание индексов на большой
# таблице) или БД временно недоступна. Иначе долгая миграция держит порт закрытым,
# платформенный health-check убивает контейнер, и получается бесконечный рестарт —
# «панель полностью не отвечает». Рассинхрон схемы, пока миграция в фоне, безопасно
# переживают сами эндпоинты (устойчивы к отсутствующим колонкам), а миграции
# идемпотентны и догоняют схему за несколько попыток.
(
  echo "=== Running Alembic migrations (background) ==="
  alembic upgrade head
  echo "=== Alembic finished (exit $?) ==="
) &

echo "=== Starting server on port ${PORT:-8000} ==="
exec uvicorn app.main:app --host 0.0.0.0 --port "${PORT:-8000}"
